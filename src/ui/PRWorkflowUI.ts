/**
 * Pull Request Workflow UI
 *
 * Single-step workflow for pushing tokens to GitHub.
 * User can choose: Push to Branch OR Create Pull Request
 */

import { ExtractionResult } from '../TokenExtractor';

// =============================================================================
// TYPES
// =============================================================================

export interface PRWorkflowOptions {
  tokenData: ExtractionResult;
  defaultBranch?: string;
  availableBranches?: string[];
  onComplete: (details: PRDetails) => void;
  onCancel: () => void;
}

export interface PRDetails {
  action: 'push-to-branch' | 'create-pr';
  branchName: string;
  commitMessage: string;
  prTitle?: string;
  prBody?: string;
  isNewBranch: boolean;
}

export interface PRSuccess {
  prNumber?: number;
  prUrl?: string;
  branchName: string;
  action: 'push-to-branch' | 'create-pr';
}

