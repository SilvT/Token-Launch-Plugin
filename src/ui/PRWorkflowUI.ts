  // =============================================================================
  // MESSAGE HANDLING
  // =============================================================================

  /**
   * Setup message handling from UI
   */
  private setupMessageHandling(): void {
    figma.ui.onmessage = async (msg) => {
      switch (msg.type) {
        case 'pr-workflow-cancel':
          this.options.onCancel();
          break;

        case 'pr-workflow-submit':
          const details: PRDetails = {
            action: msg.details.action,
            branchName: msg.details.branchName,
            commitMessage: msg.details.commitMessage,
            prTitle: msg.details.prTitle,
            prBody: this.generatePRBody(),
            isNewBranch: msg.details.isNewBranch
          };
          this.options.onComplete(details);
          break;

        case 'pr-workflow-done':
          figma.closePlugin();
          break;
      }
    };
  }

