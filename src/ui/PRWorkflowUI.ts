// =============================================================================
// TYPES
// =============================================================================

export interface PRWorkflowOptions {
  tokenData: ExtractionResult;
  documentInfo: DocumentInfo;
  defaultBranch?: string;
  availableBranches?: string[];
  onComplete: (details: PRDetails) => void;
  onCancel: () => void;
}

export interface PRDetails {
  action: 'push-to-branch' | 'create-pr';
  branchName: string;
  commitMessage: string;
  prTitle?: string;
  prBody?: string;
  isNewBranch: boolean;
  tokenFileLocation?: string;  // NEW: Optional token file location
  workflowTrigger?: WorkflowTriggerConfig;  // NEW: Optional workflow trigger config
}

export interface PRSuccess {
  prNumber?: number;
  prUrl?: string;
  branchName: string;
  action: 'push-to-branch' | 'create-pr';
  workflowTrigger?: WorkflowTriggerResult;  // NEW: Workflow trigger result
}

